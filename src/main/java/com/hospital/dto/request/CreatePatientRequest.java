package com.hospital.dto.request;

import com.hospital.entity.Gender;
import jakarta.validation.constraints.*;
import lombok.Data;

import java.time.LocalDate;

/**
 * DTO для создания нового пациента (POST /api/patients).
 *
 * <p><b>Почему DTO, а не Entity?</b><br>
 * Entity {@code Patient} содержит поля {@code id}, {@code registrationDate},
 * {@code status}, {@code active} и связи с доктором/палатой — всё это
 * устанавливается системой автоматически. Если бы мы принимали Entity напрямую,
 * клиент мог бы попытаться подставить произвольный {@code id} или установить
 * статус в обход бизнес-логики. DTO изолирует только те поля, которые
 * пользователь действительно вправе указывать при создании.
 *
 * <p>Все ограничения (аннотации валидации) проверяются фреймворком до входа
 * в сервисный метод — контроллер помечает параметр аннотацией {@code @Valid}.
 */
@Data
public class CreatePatientRequest {

    /**
     * Полное имя пациента (ФИО).
     *
     * <p><b>@NotBlank</b> — поле обязательно; null и пустая строка не допускаются.<br>
     * <b>@Size(max = 255)</b> — защита от слишком длинных строк, соответствует
     * ограничению столбца {@code VARCHAR(255)} в БД. Без этого ограничения
     * сервер базы данных бросил бы исключение уже на уровне SQL.
     */
    @NotBlank(message = "Full name is required")
    @Size(max = 255)
    private String fullName;

    /**
     * Дата рождения пациента.
     *
     * <p><b>@NotNull</b> — дата обязательна (в отличие от {@code @NotBlank},
     * работает не только со строками, но и с любым объектом).<br>
     * <b>@Past</b> — дата должна быть строго в прошлом. Это бизнес-правило:
     * пациент не может родиться в будущем. Bean Validation сравнивает
     * значение с текущей датой автоматически.
     */
    @NotNull(message = "Birth date is required")
    @Past(message = "Birth date must be in the past")
    private LocalDate birthDate;

    /**
     * Пол пациента (перечисление {@code Gender}: MALE / FEMALE).
     *
     * <p><b>@NotNull</b> — пол обязателен. Jackson десериализует строку
     * из JSON в значение enum; если строка не совпадает ни с одним
     * значением — запрос упадёт ещё до валидации с ошибкой 400.
     */
    @NotNull(message = "Gender is required")
    private Gender gender;

    /**
     * СНИЛС пациента — уникальный идентификатор в системе обязательного
     * пенсионного страхования России.
     *
     * <p><b>@NotBlank</b> — поле обязательно.<br>
     * <b>@Pattern</b> — проверяет строку на соответствие регулярному выражению.
     * Формат: {@code XXX-XXX-XXX XX} (три группы цифр через дефис и две
     * цифры через пробел). Регулярное выражение {@code \\d{3}-\\d{3}-\\d{3}\\s\\d{2}}
     * означает: три цифры, дефис, три цифры, дефис, три цифры, пробел, две цифры.
     * Такая валидация защищает от опечаток ещё до сохранения в БД.
     */
    @NotBlank(message = "SNILS is required")
    @Pattern(regexp = "\\d{3}-\\d{3}-\\d{3}\\s\\d{2}", message = "SNILS format: XXX-XXX-XXX XX")
    private String snils;

    /**
     * Номер телефона пациента (необязательное поле).
     *
     * <p><b>@Pattern</b> — при наличии значения проверяет формат телефона.
     * Регулярное выражение {@code \\+?[\\d\\-\\s()]{7,20}} допускает
     * международные форматы (с «+»), разделители (дефис, пробел, скобки)
     * и длину от 7 до 20 символов. Отсутствие {@code @NotBlank} означает,
     * что поле можно не передавать — тогда оно сохраняется как {@code null}.
     */
    @Pattern(regexp = "\\+?[\\d\\-\\s()]{7,20}", message = "Invalid phone format")
    private String phone;

    /**
     * Адрес проживания пациента (необязательное поле).
     * Не имеет ограничений Bean Validation — любая строка допустима.
     */
    private String address;
}
