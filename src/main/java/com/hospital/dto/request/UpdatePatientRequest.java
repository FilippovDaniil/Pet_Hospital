package com.hospital.dto.request;

import jakarta.validation.constraints.Pattern;
import jakarta.validation.constraints.Size;
import lombok.Data;

/**
 * DTO для частичного обновления данных пациента (PUT /api/patients/{id}).
 *
 * <p><b>Зачем отдельный DTO для обновления?</b><br>
 * В отличие от {@code CreatePatientRequest}, здесь все поля необязательны
 * (нет аннотаций {@code @NotBlank} / {@code @NotNull}). Это классический
 * паттерн «Partial Update»: клиент передаёт только те поля, которые нужно
 * изменить, остальные остаются прежними. Сервисный слой проверяет каждое
 * поле на {@code null} и обновляет Entity только при наличии нового значения.
 *
 * <p>Нельзя обновить СНИЛС, дату рождения, пол и статус через этот DTO —
 * это защита от случайного изменения критически важных данных пациента.
 * Для таких операций потребовались бы отдельные эндпоинты с доп. проверками.
 */
@Data
public class UpdatePatientRequest {

    /**
     * Новое полное имя пациента (необязательное поле).
     *
     * <p><b>@Size(max = 255)</b> — если значение передано, его длина не должна
     * превышать 255 символов (соответствует ограничению VARCHAR(255) в БД).
     * Аннотация {@code @Size} на {@code null} не срабатывает — это нормально,
     * так как поле не является обязательным.
     */
    @Size(max = 255)
    private String fullName;

    /**
     * Новый номер телефона пациента (необязательное поле).
     *
     * <p><b>@Pattern</b> — если значение передано, проверяет формат телефона.
     * Допускает международный формат, разделители и длину 7–20 символов.
     * При передаче {@code null} аннотация {@code @Pattern} игнорируется.
     */
    @Pattern(regexp = "\\+?[\\d\\-\\s()]{7,20}", message = "Invalid phone format")
    private String phone;

    /**
     * Новый адрес проживания пациента (необязательное поле).
     * Никаких ограничений — любая строка или {@code null}.
     */
    private String address;
}
