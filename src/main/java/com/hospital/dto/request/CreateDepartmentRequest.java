package com.hospital.dto.request;

import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Size;
import lombok.Data;

/**
 * DTO для создания нового отделения больницы (POST /api/departments).
 *
 * <p>Отделение — это структурная единица клиники (например, «Хирургия»,
 * «Терапия»). DTO принимает только редактируемые атрибуты; поле {@code id}
 * генерируется базой данных.
 *
 * <p>Обратите внимание на {@code headDoctorId}: заведующий отделением
 * назначается через ID уже существующего врача. Сервис проверит
 * наличие врача в БД и установит связь. Передача вложенного объекта
 * {@code DoctorResponse} здесь была бы излишней — достаточно одного
 * идентификатора.
 */
@Data
public class CreateDepartmentRequest {

    /**
     * Название отделения (например, «Кардиология», «Неврология»).
     *
     * <p><b>@NotBlank</b> — название обязательно; нельзя создать безымянное
     * отделение.<br>
     * <b>@Size(max = 255)</b> — ограничение по длине строки согласно
     * VARCHAR(255) в схеме БД.
     */
    @NotBlank(message = "Department name is required")
    @Size(max = 255)
    private String name;

    /**
     * Описание отделения — краткая информация о профиле и специализации.
     * Необязательное поле; при отсутствии сохраняется как {@code null}.
     */
    private String description;

    /**
     * Физическое расположение отделения (этаж, корпус, номер кабинета).
     * Необязательное поле; свободный текст без валидации формата.
     */
    private String location;

    /**
     * Идентификатор врача, назначаемого заведующим отделением
     * (необязательное поле).
     *
     * <p>Если {@code null} — отделение создаётся без заведующего.
     * Заведующего можно назначить позже через PUT /api/departments/{id}.
     * Сервис проверит, что врач с таким {@code id} существует и активен.
     */
    private Long headDoctorId;
}
