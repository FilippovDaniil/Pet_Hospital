package com.hospital.dto.request;

import jakarta.validation.constraints.Size;
import lombok.Data;

/**
 * DTO для частичного обновления отделения (PUT /api/departments/{id}).
 *
 * <p>Все поля необязательны — клиент передаёт только то, что нужно изменить.
 * Если поле равно {@code null}, сервис оставит текущее значение в Entity
 * без изменений (паттерн Partial Update).
 *
 * <p>Структура идентична {@code CreateDepartmentRequest}, но без
 * обязательных аннотаций — это намеренное разделение: при создании
 * название обязательно, при обновлении — опционально (нельзя случайно
 * затереть название, не передав его).
 */
@Data
public class UpdateDepartmentRequest {

    /**
     * Новое название отделения (необязательное поле).
     *
     * <p><b>@Size(max = 255)</b> — если значение передано, его длина ограничена
     * 255 символами. Аннотация безопасно игнорируется при {@code null}.
     */
    @Size(max = 255)
    private String name;

    /**
     * Новое описание отделения (необязательное поле).
     */
    private String description;

    /**
     * Новое физическое расположение отделения (необязательное поле).
     */
    private String location;

    /**
     * Идентификатор нового заведующего отделением (необязательное поле).
     *
     * <p>Если передан — текущий заведующий заменяется на указанного.
     * Сервис проверит существование врача с данным {@code id}.
     * Если {@code null} — заведующий не меняется.
     */
    private Long headDoctorId;
}
