package com.hospital.service;

import com.hospital.dto.request.CreateDepartmentRequest;
import com.hospital.dto.request.UpdateDepartmentRequest;
import com.hospital.dto.response.DepartmentResponse;

import java.util.List;

/**
 * Контракт сервисного слоя для управления отделениями больницы.
 *
 * --- Почему интерфейс, а не напрямую класс? ---
 * Помимо тестируемости и DIP (см. PatientService), есть ещё одна причина:
 * Spring создаёт прокси-объекты для классов, помеченных @Transactional.
 * Когда сервис реализует интерфейс, Spring использует JDK Dynamic Proxy —
 * это стандартный механизм проксирования через интерфейс.
 * Если интерфейса нет, Spring вынужден использовать CGLIB (байткод-проксирование),
 * что сложнее и имеет ограничения (например, класс не должен быть final).
 *
 * --- Отличие от PatientService: нет пагинации ---
 * DepartmentService возвращает List<DepartmentResponse> вместо PageResponse.
 * Это сознательное решение: отделений в больнице обычно немного (десятки, максимум сотни),
 * поэтому пагинация избыточна. Для небольших справочников проще вернуть весь список.
 *
 * --- Нет softDelete: только delete ---
 * В отличие от Patient и Doctor, для Department используется реальное удаление.
 * Однако реализация должна проверить, что в отделении нет активных врачей и палат,
 * иначе выбросить исключение — нельзя удалить отделение с сотрудниками.
 */
public interface DepartmentService {

    /**
     * Создаёт новое отделение больницы.
     *
     * Реализация:
     * - Замаппить запрос в Department.
     * - Поле headDoctor при создании может быть null (заведующий назначается позже).
     * - Сохранить и вернуть DTO.
     *
     * @param request DTO с данными отделения (название, описание)
     * @return DepartmentResponse — созданное отделение с id
     */
    DepartmentResponse create(CreateDepartmentRequest request);

    /**
     * Возвращает данные отделения по его идентификатору.
     *
     * Если отделение не найдено — выбрасывается исключение с HTTP 404.
     *
     * @param id первичный ключ отделения
     * @return DepartmentResponse — данные отделения, включая информацию о заведующем
     */
    DepartmentResponse getById(Long id);

    /**
     * Возвращает полный список всех отделений больницы.
     *
     * Используется, например, для заполнения выпадающих списков на фронтенде
     * (выбор отделения при создании врача или палаты).
     * Пагинация здесь не нужна — количество отделений в типичной больнице невелико.
     *
     * @return список всех отделений (не фильтруется по active, т.к. у Department нет soft-delete)
     */
    List<DepartmentResponse> getAll();

    /**
     * Обновляет данные существующего отделения.
     *
     * Обновляемые данные: название, описание.
     * Поле headDoctor не обновляется через этот метод — назначение заведующего
     * является отдельной привилегированной операцией (например, только для ADMIN).
     *
     * @param id      идентификатор отделения для обновления
     * @param request DTO с новыми значениями
     * @return DepartmentResponse — отделение с обновлёнными данными
     */
    DepartmentResponse update(Long id, UpdateDepartmentRequest request);

    /**
     * Физически удаляет отделение из базы данных.
     *
     * Реализация должна выполнить проверки перед удалением:
     * - Нет ли активных врачей в этом отделении.
     * - Нет ли палат, привязанных к этому отделению.
     * Нарушение этих условий должно приводить к исключению (HTTP 409 Conflict).
     *
     * Почему не softDelete? Отделения — это структурные единицы организации.
     * Если отделение ликвидировано, перед его удалением все связи должны быть
     * явно разрешены (врачи переведены, палаты переназначены).
     *
     * @param id идентификатор отделения для удаления
     */
    void delete(Long id);
}
