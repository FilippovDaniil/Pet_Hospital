package com.hospital.service.event;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.math.BigDecimal;
import java.time.LocalDateTime;

/**
 * Доменное событие: пациенту оказана платная услуга.
 *
 * =====================================================================
 * НАЗНАЧЕНИЕ И СТРУКТУРА СОБЫТИЯ
 * =====================================================================
 *
 * PaidServiceEvent публикуется при каждом назначении платной услуги пациенту.
 * Основной потребитель — биллинговая система (в данном проекте представлена
 * заглушкой в PaidServiceEventConsumer), которая должна выставить счёт
 * и зарегистрировать транзакцию.
 *
 * Подход Event-Driven позволяет биллинговому модулю работать независимо:
 * он подписан на топик paid-service-events и реагирует асинхронно,
 * не блокируя основной поток регистрации услуги.
 *
 * Обязательные поля (общие для всех событий системы):
 *
 *   eventId (String / UUID):
 *     Уникальный идентификатор события.
 *     Критически важен для биллинга: без идемпотентности одна услуга
 *     может быть выставлена к оплате дважды при повторной доставке сообщения.
 *     Потребитель проверяет eventId в outbox_event прежде чем создать счёт.
 *
 *   occurredAt (LocalDateTime):
 *     Бизнес-время оказания услуги — важно для финансовой отчётности
 *     (в каком периоде учитывать выручку).
 *
 * Бизнес-поля (специфичны для PaidServiceEvent):
 *
 *   patientId / patientName:
 *     Кому оказана услуга. Денормализованное имя экономит запрос к БД.
 *
 *   serviceId / serviceName:
 *     Какая услуга оказана. serviceName денормализован для удобства
 *     отображения в счёте без JOIN к справочнику услуг.
 *
 *   price (BigDecimal):
 *     Стоимость услуги НА МОМЕНТ её оказания. Важно фиксировать price
 *     в событии, а не читать из справочника на момент обработки:
 *     цена могла измениться, а счёт должен отражать реальную стоимость
 *     на момент транзакции.
 *
 *   linkId:
 *     ID связи пациент-услуга из таблицы-ассоциации (PatientPaidService).
 *     Нужен потребителю, чтобы при необходимости найти исходную запись
 *     в БД без полного перебора.
 */
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class PaidServiceEvent {

    // UUID события — уникальный ключ для защиты от двойного списания в биллинге.
    // Генерируется: UUID.randomUUID().toString() при создании события в сервисе.
    private String eventId;

    // Время оказания услуги — фиксируется для финансового учёта и аудита.
    private LocalDateTime occurredAt;

    // ID пациента — ссылка на запись в таблице patient.
    private Long patientId;

    // Имя пациента — денормализовано, чтобы биллинг не делал лишний запрос за именем.
    private String patientName;

    // ID услуги — ссылка на справочник платных услуг.
    private Long serviceId;

    // Название услуги — денормализовано, используется при формировании счёта.
    private String serviceName;

    // Стоимость услуги на момент оказания (не берётся из справочника при обработке,
    // потому что цена могла измениться — используем значение из события).
    private BigDecimal price;

    // ID записи в таблице-связке PatientPaidService — для обратной трассировки.
    private Long linkId;
}
