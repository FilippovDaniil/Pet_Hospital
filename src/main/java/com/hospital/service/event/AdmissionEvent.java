package com.hospital.service.event;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.time.LocalDateTime;

/**
 * Доменное событие: госпитализация или выписка пациента из палаты.
 *
 * =====================================================================
 * СТРУКТУРА СОБЫТИЯ И СМЫСЛ ПОЛЕЙ
 * =====================================================================
 *
 * Событие AdmissionEvent публикуется всякий раз, когда пациент
 * заселяется в палату (ADMITTED) или покидает её (DISCHARGED).
 * Это событие реализует принцип "eventual consistency" (конечной согласованности):
 * другие части системы (например, модуль статистики занятости палат,
 * биллинг или уведомления) узнают о смене состояния через Kafka,
 * а не через прямые вызовы или синхронные HTTP-запросы.
 *
 * Обязательные поля (общие для всех событий системы):
 *
 *   eventId (String / UUID):
 *     Уникальный идентификатор конкретного события.
 *     Генерируется как UUID.randomUUID().toString() при создании.
 *     Используется потребителем для проверки идемпотентности:
 *     если событие с таким eventId уже есть в outbox_event и помечено
 *     processed=true — дублирующее сообщение из Kafka будет отброшено.
 *
 *   occurredAt (LocalDateTime):
 *     Бизнес-время события — когда факт госпитализации/выписки произошёл.
 *     Нужен для упорядочивания событий при аудите и восстановлении
 *     хронологии в случае out-of-order доставки из Kafka.
 *
 * Бизнес-поля (специфичны для AdmissionEvent):
 *
 *   patientId / patientName:
 *     Кто был госпитализирован/выписан. patientName денормализован
 *     (скопирован из сущности Patient) чтобы потребитель мог отобразить
 *     имя без дополнительного запроса к сервису пациентов.
 *
 *   wardId / wardNumber:
 *     Какая палата задействована. wardNumber (строковый номер) также
 *     денормализован по той же причине.
 *
 *   action (enum Action):
 *     Тип действия: ADMITTED (заселение) или DISCHARGED (выписка).
 *     Позволяет потребителю применить разную логику без разбора остальных полей.
 *
 * Почему Action — вложенный enum, а не отдельное поле eventType (String)?
 *   Оба подхода допустимы. Вложенный enum строже типизирован — компилятор
 *   не позволит передать произвольную строку. eventType (String) удобнее,
 *   если событие может иметь много типов и они расширяются динамически.
 *   В данном случае действий ровно два, поэтому enum предпочтительнее.
 */
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class AdmissionEvent {

    /**
     * Тип действия с палатой.
     * ADMITTED  — пациент заселён в палату.
     * DISCHARGED — пациент выписан из палаты.
     */
    public enum Action { ADMITTED, DISCHARGED }

    // UUID события — гарантирует уникальность и служит ключом идемпотентности
    // на стороне потребителя (проверяется в outbox_event).
    private String eventId;

    // Бизнес-время события: когда произошла госпитализация/выписка.
    // Устанавливается в сервисе как LocalDateTime.now() перед публикацией.
    private LocalDateTime occurredAt;

    // ID пациента — внешний ключ к таблице patient.
    private Long patientId;

    // Имя пациента — денормализованное поле для потребителей,
    // которым не нужно делать отдельный запрос за именем.
    private String patientName;

    // ID палаты — внешний ключ к таблице ward.
    private Long wardId;

    // Строковый номер палаты (например, "101A") — денормализован для удобства отображения.
    private String wardNumber;

    // Тип действия: пациент въехал (ADMITTED) или выехал (DISCHARGED).
    private Action action;
}
