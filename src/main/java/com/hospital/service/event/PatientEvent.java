package com.hospital.service.event;

import com.hospital.entity.PatientStatus;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.time.LocalDateTime;

/**
 * Доменное событие: изменение состояния пациента.
 *
 * =====================================================================
 * СТРУКТУРА ДОМЕННОГО СОБЫТИЯ (Domain Event)
 * =====================================================================
 *
 * Доменное событие — это неизменяемый факт о том, что произошло в системе.
 * Оно отвечает на вопрос "что случилось?", а не "что делать дальше?".
 *
 * Обязательные поля (присутствуют во всех событиях системы):
 *
 *   eventId (String / UUID):
 *     Уникальный идентификатор ЭТОГО конкретного события.
 *     Генерируется как UUID.randomUUID().toString() при создании события.
 *     Зачем нужен:
 *       - Идемпотентность на стороне потребителя: если то же самое сообщение
 *         придёт из Kafka дважды (Kafka гарантирует at-least-once delivery),
 *         потребитель проверит outbox_event.eventId и откажется обрабатывать дубль.
 *       - Трассировка: по eventId можно найти событие в логах, в outbox_event,
 *         в системах мониторинга (Grafana, ELK).
 *       - Ключ Kafka-сообщения: eventId используется как ключ партиционирования,
 *         что гарантирует, что все события с одним ключом попадут в одну партицию.
 *
 *   eventType (String):
 *     Строковое описание типа события, например "PATIENT_CREATED", "STATUS_CHANGED".
 *     Зачем нужен:
 *       - Один топик может нести несколько типов событий; потребитель смотрит
 *         на eventType, чтобы понять, какую логику применить.
 *       - Полезен для фильтрации при повторной обработке из outbox_event.
 *
 *   occurredAt (LocalDateTime):
 *     Метка времени момента, когда событие произошло в бизнес-домене.
 *     НЕ путать со временем отправки в Kafka или временем обработки потребителем.
 *     Зачем нужна:
 *       - Позволяет восстановить хронологию событий даже если они пришли
 *         к потребителю не в том порядке (out-of-order delivery).
 *       - Используется для аудита и отчётности.
 *
 * Бизнес-поля этого события (специфичны для PatientEvent):
 *
 *   patientId:      идентификатор пациента в БД
 *   patientName:    имя пациента (денормализация — чтобы потребителю не нужно
 *                   было делать лишний запрос в БД только ради имени)
 *   newStatus:      новый статус пациента после изменения (PatientStatus enum)
 *   previousDoctorId / newDoctorId:
 *                   кому был и кому стал назначен пациент (null если не менялось)
 */
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class PatientEvent {

    // UUID события — уникальный идентификатор для идемпотентности и трассировки.
    // Генерируется в бизнес-сервисе: UUID.randomUUID().toString()
    private String eventId;

    // Тип произошедшего действия, например: "PATIENT_CREATED", "STATUS_CHANGED", "DOCTOR_ASSIGNED".
    // Позволяет потребителю понять смысл события без разбора всех полей.
    private String eventType;

    // Момент времени, когда событие случилось в домене.
    // Устанавливается в сервисе как LocalDateTime.now() до записи в БД.
    private LocalDateTime occurredAt;

    // Идентификатор пациента — связывает событие с конкретной записью в таблице patient.
    private Long patientId;

    // Имя пациента — денормализованное поле для удобства потребителей,
    // которым нужно отобразить имя без дополнительного запроса в БД.
    private String patientName;

    // Новый статус пациента после изменения (enum: ACTIVE, DISCHARGED, и т.д.).
    private PatientStatus newStatus;

    // Идентификатор предыдущего врача (null, если врач не менялся или не был назначен).
    private Long previousDoctorId;

    // Идентификатор нового назначенного врача (null, если переназначения не было).
    private Long newDoctorId;
}
