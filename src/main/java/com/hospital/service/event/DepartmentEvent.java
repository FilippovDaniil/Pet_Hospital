package com.hospital.service.event;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

import java.time.LocalDateTime;

/**
 * Доменное событие: изменение данных отделения больницы.
 *
 * =====================================================================
 * НАЗНАЧЕНИЕ И СТРУКТУРА СОБЫТИЯ
 * =====================================================================
 *
 * DepartmentEvent публикуется при создании, обновлении или удалении отделения.
 * Отделение — это справочная сущность (Department), и её изменения могут
 * интересовать несколько других модулей: управление врачами, статистику,
 * отчётность, административный интерфейс.
 *
 * Публикация через Kafka (а не синхронный вызов) обеспечивает слабую связанность:
 * модуль отделений не знает ничего о подписчиках — он просто публикует факт.
 *
 * Обязательные поля (общие для всех событий системы):
 *
 *   eventId (String / UUID):
 *     Уникальный идентификатор события.
 *     Критически важен для идемпотентности: Kafka гарантирует доставку
 *     "at least once" (хотя бы один раз), а значит одно и то же событие
 *     может прийти потребителю дважды. eventId позволяет обнаружить дубль
 *     и отбросить повторную обработку (проверка в outbox_event.eventId).
 *
 *   eventType (String):
 *     Тип операции над отделением: "DEPARTMENT_CREATED", "DEPARTMENT_UPDATED",
 *     "DEPARTMENT_DELETED". Несколько типов событий путешествуют по одному топику
 *     department-events — eventType позволяет потребителю выбрать нужную обработку.
 *
 *   occurredAt (LocalDateTime):
 *     Метка времени, когда изменение произошло в бизнес-домене.
 *     Используется при построении хронологии изменений (audit log) и
 *     для корректной обработки out-of-order сообщений (если потребитель
 *     получил два события и нужно выяснить, какое из них позже).
 *
 * Бизнес-поля:
 *
 *   departmentId:    ID отделения — первичный ключ в таблице department.
 *   departmentName:  Название отделения (денормализовано), чтобы потребители
 *                    могли отобразить его без JOIN или дополнительного запроса.
 */
@Data
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class DepartmentEvent {

    // UUID события — уникальный идентификатор для защиты от двойной обработки.
    // Проверяется потребителем: найдена ли запись с таким eventId в outbox_event
    // и помечена ли она как processed=true.
    private String eventId;

    // Тип операции: "DEPARTMENT_CREATED" / "DEPARTMENT_UPDATED" / "DEPARTMENT_DELETED".
    // Позволяет одному потребителю обрабатывать разные события из одного топика.
    private String eventType;

    // Бизнес-время события. Устанавливается в сервисе при формировании объекта события.
    private LocalDateTime occurredAt;

    // ID отделения — ссылка на запись в таблице department.
    private Long departmentId;

    // Название отделения — денормализовано для удобства потребителей.
    // Например: "Хирургия", "Терапия", "Реанимация".
    private String departmentName;
}
