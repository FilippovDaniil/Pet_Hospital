package com.hospital.entity;

/**
 * Статус пребывания пациента в стационаре.
 *
 * <p>Моделирует жизненный цикл госпитализации: пациент поступает на лечение (TREATMENT),
 * затем либо выписывается (DISCHARGED), либо переводится в другое отделение/учреждение (TRANSFERRED).
 *
 * <p>Архитектурная заметка: вместо хранения статуса строкой используется enum, чтобы:
 * <ul>
 *   <li>Исключить опечатки («dischargd» вместо «discharged»).</li>
 *   <li>Гарантировать полноту обработки во {@code switch}-выражениях — компилятор предупредит
 *       о непокрытых ветках.</li>
 *   <li>Упростить фильтрацию в JPQL/Criteria API: {@code WHERE p.status = :status}.</li>
 * </ul>
 *
 * <p>В базе данных хранится текстовым значением (EnumType.STRING), что указывается
 * аннотацией {@code @Enumerated(EnumType.STRING)} на поле сущности.
 */
public enum PatientStatus {

    /** Пациент проходит лечение в стационаре. Начальный статус при госпитализации. */
    TREATMENT,

    /** Пациент выписан из стационара. Конечный статус нормального завершения лечения. */
    DISCHARGED,

    /** Пациент переведён в другое отделение или медицинское учреждение. */
    TRANSFERRED
}
