package com.hospital.entity;

/**
 * Перечисление биологического пола пациента.
 *
 * <p>Используется в сущности {@link Patient} вместо строки или булевого флага по нескольким причинам:
 * <ul>
 *   <li>Безопасность типов: компилятор не позволит передать произвольную строку вместо пола.</li>
 *   <li>Читаемость: MALE/FEMALE понятнее, чем 0/1 или "M"/"F".</li>
 *   <li>В базу данных сохраняется как строка благодаря аннотации
 *       {@code @Enumerated(EnumType.STRING)} на стороне сущности — это удобнее при отладке
 *       SQL-запросов и переносимее, чем числовой индекс {@code EnumType.ORDINAL}
 *       (при изменении порядка констант ordinal-значения сломаются).</li>
 * </ul>
 */
public enum Gender {
    /** Мужской пол. */
    MALE,
    /** Женский пол. */
    FEMALE
}
