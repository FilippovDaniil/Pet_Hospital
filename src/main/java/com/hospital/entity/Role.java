package com.hospital.entity;

/**
 * Роль пользователя системы для разграничения прав доступа (RBAC — Role-Based Access Control).
 *
 * <p>Используется совместно со Spring Security: при аутентификации роль преобразуется
 * в объект {@link org.springframework.security.core.authority.SimpleGrantedAuthority},
 * который Spring Security проверяет при каждом запросе к защищённому ресурсу.
 *
 * <p>Соглашение об именовании: префикс {@code ROLE_} обязателен для корректной работы
 * метода {@code hasRole("ADMIN")} в Spring Security — фреймворк автоматически добавляет
 * этот префикс при проверке. Если использовать {@code hasAuthority("ROLE_ADMIN")},
 * префикс указывается явно.
 *
 * <p>Текущая модель прав:
 * <ul>
 *   <li>ROLE_ADMIN — полный доступ ко всем операциям системы.</li>
 *   <li>ROLE_DOCTOR — доступ к медицинским данным пациентов, назначению услуг.</li>
 *   <li>ROLE_NURSE — ограниченный доступ: размещение пациентов, базовые операции.</li>
 * </ul>
 */
public enum Role {

    /**
     * Администратор системы.
     * Имеет полный доступ: управление пользователями, справочниками, отделениями.
     */
    ROLE_ADMIN,

    /**
     * Врач.
     * Управляет своими пациентами, назначает платные услуги, ведёт историю лечения.
     */
    ROLE_DOCTOR,

    /**
     * Медицинская сестра.
     * Размещает пациентов по палатам, ведёт учёт текущей заполненности.
     */
    ROLE_NURSE
}
