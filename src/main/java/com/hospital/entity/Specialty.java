package com.hospital.entity;

/**
 * Медицинская специализация врача.
 *
 * <p>Используется в сущности {@link Doctor} для классификации врачей по профилю.
 * Хранится в базе данных как строка (EnumType.STRING), что позволяет легко фильтровать
 * врачей по специальности через JPQL или Criteria API без числовых кодов.
 *
 * <p>Расширение: при необходимости добавить новую специальность достаточно добавить
 * константу в этот enum — никаких изменений схемы БД не требуется (столбец типа VARCHAR).
 */
public enum Specialty {

    /** Кардиолог — специалист по заболеваниям сердечно-сосудистой системы. */
    CARDIOLOGIST,

    /** Хирург — врач, выполняющий оперативные вмешательства. */
    SURGEON,

    /** Терапевт — врач общей практики, первичное звено медицинской помощи. */
    THERAPIST,

    /** Невролог — специалист по заболеваниям нервной системы. */
    NEUROLOGIST,

    /** Педиатр — врач, специализирующийся на лечении детей. */
    PEDIATRICIAN,

    /** Ортопед — специалист по заболеваниям опорно-двигательного аппарата. */
    ORTHOPEDIST,

    /** Онколог — специалист по диагностике и лечению опухолей. */
    ONCOLOGIST,

    /** Уролог — специалист по заболеваниям мочеполовой системы. */
    UROLOGIST
}
